    private void mettresProtectorSubmitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mettresProtectorSubmitButtonActionPerformed
        MettresProtectorViewModel mCost = new MettresProtectorViewModel();
        String productRange = (String) mettresProtectorProductRangeCombo.getSelectedItem();
        mCost.setProductRange(productRange);

        String materialType = (String) mettresProtectorMaterialTypeCombo.getSelectedItem();
        mCost.setMaterialType(materialType);

        String size = (String) mettresProtectorSizeCombo.getSelectedItem();
        mCost.setSize(size);

        String paddingType = (String) mettresProtectorPaddingTypeCombo.getSelectedItem();
        mCost.setPaddingType(paddingType);

        String taffataType = (String) mettresProtectorTaffataTypeCombo.getSelectedItem();
        mCost.setTaffataType(taffataType);

        try {
            double withQuiltingShrinkage = Double.parseDouble(mettresProtectorWidthQuiltingShrinkage.getText());
            if (withQuiltingShrinkage < 0 || withQuiltingShrinkage > 20) {
                JOptionPane.showMessageDialog(this, "Width quilting shrinkage should be between 0 and 20 %");
                return;
            }
            mCost.setWidthQuiltyShrinkage(withQuiltingShrinkage);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid width quilting shrinkage value");
            return;
        }

        try {
            double margin = Double.parseDouble(mattressProtectorMarginField.getText());
            if (margin < 0) {
                JOptionPane.showMessageDialog(this, "Profit margin should be a positive value");
                return;
            }
            mCost.setMargin(margin);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid margin value");
            return;
        }

        try {
            double taxRate = Double.parseDouble(mattressProtectorTaxField.getText());
            if (taxRate < 0) {
                JOptionPane.showMessageDialog(this, "Tax rate should be a positive value");
                return;
            }
            mCost.setTaxRate(taxRate);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid tax rate value");
            return;
        }

        try {
            double heightQuiltingShrinkage = Double.parseDouble(mettresProtectorHeightQuiltiingShrinkage.getText());
            if (heightQuiltingShrinkage < 0 || heightQuiltingShrinkage > 20) {
                JOptionPane.showMessageDialog(this, "Width quilting shrinkage should be between 0 and 20 %");
                return;
            }
            mCost.setHeightQuiltyShrinkage(heightQuiltingShrinkage);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid height quilting shrinkage value");
            return;
        }

        try {
            double fabricWastage = Double.parseDouble(mettresProtectorFabricWastage.getText());
            if (fabricWastage < 0 || fabricWastage > 10) {
                JOptionPane.showMessageDialog(this, "Fabric wastage should be between 0 and 10 %");
                return;
            }
            mCost.setFabricWastage(fabricWastage);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid fabric wastage value");
            return;
        }


        try {
            double taffataWastage = Double.parseDouble(mettresProtectorTaffateWastage.getText());
            if (taffataWastage < 0 || taffataWastage > 20) {
                JOptionPane.showMessageDialog(this, "Taffata wastage should be between 0 and 20 %");
                return;
            }
            mCost.setTaffataWastage(taffataWastage);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid taffta wastage value");
            return;
        }


        try {
            double paddingWastage = Double.parseDouble(mettresProtectorPaddingWastage.getText());
            if (paddingWastage < 0 || paddingWastage > 20) {
                JOptionPane.showMessageDialog(this, "Padding wastage should be between 0 and 20 %");
                return;
            }
            mCost.setPaddingWastage(paddingWastage);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Please enter a valid padding wastage value");
            return;
        }

        boolean useCustom = mettresProtectorUseCustom.isSelected();
        if (useCustom) {
            mCost.setIsCustom(useCustom);
            try {
                Double width = Double.parseDouble(mettresProtectorCustomWidth.getText());
                if (width <= 0 || width > 120) {
                    JOptionPane.showMessageDialog(this, "Width should be between 0 and 120");
                    return;
                }
                mCost.setCustomWidth(width);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Please enter a valid width value");
                return;
            }

            try {
                Double height = Double.parseDouble(mettresProtectorCustomHeight.getText());
                if (height <= 0 || height > 120) {
                    JOptionPane.showMessageDialog(this, "Height should be between 0 and 120");
                    return;
                }
                mCost.setCustomHeight(height);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Please enter a valid height value");
                return;
            }

            mCost.setIncludeLable(mettresProtectorIncludeLable.isSelected());
            mCost.setIncludeTag(mettresProtectorIncludeTag.isSelected());
            mCost.setIncludePEBag(mettresProtectorIncludePEBag.isSelected());
            mCost.setIncludeNonWovenBag(mettresProtectorIncludeNonWoven.isSelected());
            mCost.setIncludePiping(mettresProtectorIncludePiping.isSelected());
        }
        DecimalFormat format = new DecimalFormat();
        format.setMaximumFractionDigits(2);
        format.setMinimumFractionDigits(2);

        MettresProtectorViewModel mReturn = MattresProtectorLogic.getCostPerUnit(mCost);
        mettresProtectorFabricCost.setText(format.format(mReturn.getFabricCost()) + "");
        mettresProtectorPaddingCost.setText(format.format(mReturn.getPaddingCost()) + "");
        mettresProtectorTaffataCost.setText(format.format(mReturn.getTaffataCost()) + "");
        mettresProtectorThreadCost.setText(format.format(mReturn.getThreadCost()) + "");
        mettresProtectorLableCost.setText(format.format(mReturn.getLableCost()) + "");
        mettresProtectorTagCost.setText(format.format(mReturn.getTagCost()) + "");
        mettresProtectorElasticCost.setText(format.format(mReturn.getElasticCost()) + "");
        mettresProtectorPipingCost.setText(format.format(mReturn.getPipingCost()) + "");
        mettresProtectorPEBagCost.setText(format.format(mReturn.getPEBagCost()) + "");
        mettresProtectorNonWovenBagCost.setText(format.format(mReturn.getNonWovenBagCost()) + "");
        mettresProtectorTotalMaterialCost.setText(format.format(mReturn.getTotalMaterialCost()) + "");
        mettresProtectorPOH.setText(format.format(mReturn.getPohCost()) + "");
        mettresProtectorLabourCost.setText(format.format(mReturn.getLabourCost()) + "");
        mettresProtectorToatalCost.setText(format.format(mReturn.getTotalCost()) + "");
        mettresProtectorFabricPaddingCutWidth.setText(format.format(mReturn.getFabricPaddingCuttingWidth()) + "");
        mettresProtectorFabricPaddingCutHeight.setText(format.format(mReturn.getFabricPaddingCuttingHeight()) + "");
        mettresProtectorTafffataCutWidth.setText(format.format(mReturn.getTaffataCuttingWidth()) + "");
        mettresProtectorTaffataCutHeight.setText(format.format(mReturn.getTaffataCuttingHeight()) + "");
        mettresProtectorSMVValue.setText(format.format(mReturn.getSmvValue()) + "");
        mettresProtectorNetSellingPrice.setText(format.format(mReturn.getNetSellingPrice()));
        mettresProtectorTaxes.setText(format.format(mReturn.getTaxes()));
        mettresProtectorGrossSellingPrice.setText(format.format(mReturn.getGrossSellingPrice()));

        //export model
        model = new ExportModel();
        model.setProductName("Mattress Protector");
        model.setProductRange(mReturn.getProductRange());
        if(mReturn.isIsCustom()){
            model.setProductSize(mReturn.getCustomWidth()+"X"+mReturn.getCustomHeight());
        }else{
            model.setProductSize(mReturn.getSize());
        }
        model.setTotalMaterialCost(mReturn.getTotalMaterialCost());
        model.setLabourCost(mReturn.getLabourCost());
        model.setProductionOverHead(mReturn.getPohCost());
        model.setTotalCostPerUnit(mReturn.getTotalCost());
        model.setNetSellingPrice(mReturn.getNetSellingPrice());
        model.setTaxes(mReturn.getTaxes());
        model.setGrossSellingPrice(mReturn.getGrossSellingPrice());


        mettresProtectorSubmitButton.setVisible(false);
        mattressProtectorNewCostingButton.setVisible(true);
        mattressProtectorCPUPanel.setVisible(true);

        //disabling input fields
        mettresProtectorProductRangeCombo.setEnabled(false);
        mettresProtectorMaterialTypeCombo.setEnabled(false);
        mettresProtectorSizeCombo.setEnabled(false);
        mettresProtectorPaddingTypeCombo.setEnabled(false);
        mettresProtectorTaffataTypeCombo.setEnabled(false);

        mettresProtectorWidthQuiltingShrinkage.setEnabled(false);
        mettresProtectorHeightQuiltiingShrinkage.setEnabled(false);
        mettresProtectorFabricWastage.setEnabled(false);
        mettresProtectorTaffateWastage.setEnabled(false);
        mettresProtectorPaddingWastage.setEnabled(false);

        mettresProtectorUseCustom.setEnabled(false);
        mettresProtectorCustomWidth.setEnabled(false);
        mettresProtectorCustomHeight.setEnabled(false);

        mettresProtectorIncludeLable.setEnabled(false);
        mettresProtectorIncludeTag.setEnabled(false);
        mettresProtectorIncludePEBag.setEnabled(false);
        mettresProtectorIncludeNonWoven.setEnabled(false);
        mettresProtectorIncludePiping.setEnabled(false);
        mattressProtectorMarginField.setEnabled(false);
        mattressProtectorTaxField.setEnabled(false);
    }//GEN-LAST:event_mettresProtectorSubmitButtonActionPerformed

